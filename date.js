// Date() - JavaScript has a built-in Date object to work with dates and times.
//          It can show current date/time, extract year/month/day, add/subtract time, format dates, etc.

let CureentDate=new Date();
console.log(CureentDate.getDate());
console.log(CureentDate.getMonth()+1);
console.log(CureentDate.getFullYear());
console.log(CureentDate.getHours());
console.log(CureentDate.getMinutes());
console.log(CureentDate.getSeconds());
console.log(CureentDate.getMilliseconds());